import 'package:flutter/material.dart';
class ThemeColor {
  static themeColor(_theme) {
    if (_theme == 'purple') {
      return Colors.purple;
    }else if(_theme == 'green') {
      return Colors.green;
    }else if(_theme == 'pink') {
      return Colors.pink;
    }else if(_theme == 'blueGrey') {
      return Colors.blueGrey;
    }else if(_theme == 'red') {
      return Colors.red;
    }else if(_theme == 'blue') {
      return Colors.blue;
    }else if(_theme == 'deepOrange') {
      return Colors.deepOrange;
    }else if(_theme == 'teal') {
      return Colors.teal;
    }else if(_theme == 'indigo') {
      return Colors.indigo;
    }else if(_theme == 'cyan') {
      return Colors.cyan;
    }
  }
}
